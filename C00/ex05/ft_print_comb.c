void ft_print_comb(void)
{
	int i = 48;
	int j = i + 1;
	int k = j + 1;
	while(i<58)
	{
		while(j<58)
		{
			while(k<58)
			{
				if(i<j<k)
				{
				write(1,&i,1);
				write(1,&j,1);
				write(1,&k,1);
				write(1,",",2);
				}
				k++;
			}
			j++;
			k = j + 1;
		}
		i++;
		j = i + 1;
		k = j + 1;
	}
}
